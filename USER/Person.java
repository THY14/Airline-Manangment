public class Person {
      int id;
      String firstname;
      String lastname;
      String tel;
      String email;
      String gender;
      String nationality;
      String dob;
     public Person(int id, String firstname, String lastname, String tel, String email, String gender, String nationality,String dob) {
         this.id = id;
         this.firstname = firstname;
         this.lastname = lastname;
         this.tel = tel;
         this.email = email;
         this.gender = gender;
         this.nationality = nationality;
         this.dob = dob;
     }
}

